public class PrimitivosBoleanos{
    public static void main(String[] args) {

        boolean  dato = false;
        System.out.println("dato = " + dato);

        double d = 98541.47e-2;
        float f = 1.2365e2f;

        //condiciones booleanas
        dato = (d > f);
        System.out.println("dato = " + dato);

        boolean esIgual = (3+1 > 2);
        System.out.println("esIgual = " + esIgual);


    }
}
